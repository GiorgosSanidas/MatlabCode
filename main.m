

function main()
    % Βάρος ενός κόκκου ρυζιού σε mg
    weight_per_rice = 25; % σε mg
    
    % Παραγωγή ρυζιού σε τόνους ανά έτος
    annual_production = 1000; % σε τόνους
    
    % Αριθμός των τετραγώνων της σκακιέρας
    num_squares = 64;
    
    % Αρχικοποίηση μεταβλητής για τον αριθμό των κόκκων ρυζιού
    total_rice = 0;
    
    % Αρχικοποίηση λίστας για τον αριθμό των κόκκων ρυζιού σε κάθε τετράγωνο
    rice_per_square = zeros(1, num_squares);
    
    % Επανάληψη για κάθε τετράγωνο της σκακιέρας
    for square = 1:num_squares
        % Υπολογισμός αριθμού κόκκων ρυζιού για το τρέχον τετράγωνο
        rice_on_square = 2^(square - 1);
        
        % Εκτύπωση του αριθμού των κόκκων ρυζιού για το τρέχον τετράγωνο
        fprintf('Οι κόκκοι είναι: %d\n', rice_on_square);
        
        % Αποθήκευση αριθμού κόκκων ρυζιού για το τρέχον τετράγωνο
        rice_per_square(square) = rice_on_square;
        
        % Αύξηση του συνολικού αριθμού των κόκκων ρυζιού
        total_rice = total_rice + rice_on_square;
    end
    
    % Υπολογισμός του συνολικού βάρους των κόκκων ρυζιού σε mg
    total_rice_mg = total_rice * weight_per_rice;
    
    % Μετατροπή του συνολικού βάρους των κόκκων ρυζιού από mg σε τόνους
    total_rice_tons = total_rice_mg / 1e6;
    
    % Εκτύπωση του συνολικού βάρους των κόκκων ρυζιού σε τόνους
    fprintf('Συνολικός αριθμός κόκκων ρυζιού που χρειάζονται: %.2f τόνοι\n', total_rice_tons);
    
    % Υπολογισμός του αριθμού των χρόνων που απαιτούνται για να πληρωθεί ο σοφός
    years_needed = total_rice_tons / annual_production;
    
    % Εκτύπωση του αριθμού των χρόνων που απαιτούνται
    fprintf('Χρόνια που απαιτούνται για να πληρωθεί ο σοφός: %.2f χρόνια\n', years_needed);
    
    % Απεικόνιση του αριθμού των κόκκων ρυζιού σε κάθε τετράγωνο
    plot(1:num_squares, rice_per_square, 'o-', 'LineWidth', 2);
    hold on;
    % Απεικόνιση της ετήσιας παραγωγής ρυζιού
    plot(1:num_squares, annual_production * ones(1, num_squares), 'r--', 'LineWidth', 2);
    
    % Ετικέτες για τους άξονες και τον τίτλο
    xlabel('Τετράγωνο');
    ylabel('Αριθμός Κόκκων Ρυζιού');
    title('Αριθμός Κόκκων Ρυζιού σε κάθε Τετράγωνο της Σκακιέρας');
    
    % Εμφάνιση του legend
    legend('Κόκκοι Ρυζιού', 'Ετήσια Παραγωγή Ρυζιού');
    grid on;
    hold off;
end
